    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
       /*  if (jTextTipo.getText().isEmpty() || jTextNombre.getText().isEmpty() || jTextApellido.getText().isEmpty() || jTextUsuario.getText().isEmpty() ) {
            JOptionPane.showMessageDialog(this, "No debe dejar campos vacios");
            return;
        }*/
        if (!edit) {
            us = new Usuario();
        }
        //us.setUsr_id(Integer.parseInt(jTextId.getText()));
        us.setTipo_usr(jTextTipo.getText());
        us.setNombre(jTextNombre.getText());
        us.setApellido(jTextApellido.getText());
        us.setUsuario(jTextUsuario.getText());
        us.setContrasena(jTextContra.getText());
        

            if (us.save()){
                JOptionPane.showMessageDialog(this, (edit ? "Editado" : "Creado") + " correctamente");
                jTextTipo.setText("");
                jTextNombre.setText("");
                jTextApellido.setText("");
                jTextUsuario.setText("");
                jTextContra.setText("");
                edit = false;
            }else{
                JOptionPane.showMessageDialog(this, "Error al registrar Usuario");
                return;
            }
            showData();
        
    }//GEN-LAST:event_jButton1MouseClicked

