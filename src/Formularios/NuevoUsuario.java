    void showData() {
        DefaultTableModel model = (DefaultTableModel) jTableUsuario.getModel();
        dataList = (new Usuario()).list();
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }

        for (Usuario s : dataList) {
            Object[] row = {s.getUsr_id(), s.getTipo_usr(), s.getNombre(), s.getApellido(), s.getUsuario(), s.getContrasena()};
            model.addRow(row);
        }
    }

